fruits = ["apple", "banana", "orange", "pear"]

for i, fruit in enumerate(fruits):
    print(i, fruit)

"""In this example, we have a list of fruits, and we want to print each fruit along with its index in the list. 
We use the enumerate function to iterate over the list and get both the index and the value of each element. 
The enumerate function takes an iterable as an argument, and returns a sequence of (index, value) tuples."""
